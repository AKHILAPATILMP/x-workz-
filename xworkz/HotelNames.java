
//1. Program to store 10 hotel names and print them on the console:

public class HotelNames {
    public static void main(String[] args) {
        String[] hotels = {"Taj", "Oberoi", "Marriott", "Hilton", "Hyatt", "Radisson", "Sheraton", "ITC", "Leela", "Novotel"};

        System.out.println("Hotel Names:");
        for (String hotel : hotels) {
            System.out.println(hotel);
        }
    }
}

