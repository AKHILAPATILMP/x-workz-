//4. Program to store 10 characters and print them:

public class Characters {
    public static void main(String[] args) {
        char[] chars = {'A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J'};

        System.out.println("Characters:");
        for (char ch : chars) {
            System.out.println(ch);
        }
    }
}