package com.xworkz.isarelation.coffeemaker;

public class Coffeemaker {
        public Coffeemaker() {
            System.out.println("Coffeemaker constructor is running");
        }

        public void powerOn() {
            System.out.println("Coffeemaker is turning on");
        }

        public void powerOff() {
            System.out.println("Coffeemaker is turning off");
        }

        public void brewCoffee() {
            System.out.println("Coffeemaker is brewing coffee");
        }

        public void setTimer() {
            System.out.println("Coffeemaker timer is set");
        }

        public void stopBrewing() {
            System.out.println("Coffeemaker has stopped brewing");
        }
    }

