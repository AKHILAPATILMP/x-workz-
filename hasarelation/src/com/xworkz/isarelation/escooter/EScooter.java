public class EScooter {

        public EScooter() {
            System.out.println("EScooter constructor is running");
        }

        public void start() {
            System.out.println("E-Scooter is starting");
        }

        public void accelerate() {
            System.out.println("E-Scooter is accelerating");
        }

        public void brake() {
            System.out.println("E-Scooter is applying brakes");
        }

        public void charge() {
            System.out.println("E-Scooter is charging");
        }

        public void stop() {
            System.out.println("E-Scooter is stopping");
        }
    }


