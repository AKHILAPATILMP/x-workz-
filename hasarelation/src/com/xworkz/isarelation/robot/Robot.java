package com.xworkz.isarelation.robot;

public class Robot {
        public Robot() {
            System.out.println("Robot constructor is running");
        }

        public void start() {
            System.out.println("Robot: Starting the system...");
        }

        public void move() {
            System.out.println("Robot: Moving forward...");
        }

        public void chargeBattery() {
            System.out.println("Robot: Charging battery...");
        }

        public void performTask() {
            System.out.println("Robot: Performing a task...");
        }

        public void shutdown() {
            System.out.println("Robot: Shutting down...");
        }
    }

