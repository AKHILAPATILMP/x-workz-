package com.xworkz.isarelation.speaker;

public class Speaker {
        public Speaker() {
            System.out.println("Speaker constructor is running");
        }

        public void powerOn() {
            System.out.println("Speaker is powered on");
        }

        public void playSound() {
            System.out.println("Speaker is playing sound");
        }

        public void adjustVolume() {
            System.out.println("Speaker volume is being adjusted");
        }

        public void mute() {
            System.out.println("Speaker is muted");
        }

        public void powerOff() {
            System.out.println("Speaker is powered off");
        }
    }

