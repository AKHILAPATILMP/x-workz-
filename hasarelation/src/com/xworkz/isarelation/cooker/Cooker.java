package com.xworkz.isarelation.cooker;

public class Cooker {
        public Cooker() {
            System.out.println("Cooker constructor is running");
        }

        public void powerOn() {
            System.out.println("Cooker is turning on");
        }

        public void powerOff() {
            System.out.println("Cooker is turning off");
        }

        public void cookFood() {
            System.out.println("Cooker is cooking food");
        }

        public void setTimer() {
            System.out.println("Cooker timer is set");
        }

        public void stopCooking() {
            System.out.println("Cooker has stopped cooking");
        }
    }

