package com.xworkz.isarelation.bicycle;

public class Bicycle {
        public Bicycle() {
            System.out.println("Bicycle constructor is running");
        }

        public void start() {
            System.out.println("Bicycle is starting");
        }

        public void pedal() {
            System.out.println("Bicycle is being pedaled");
        }

        public void brake() {
            System.out.println("Bicycle is applying brakes");
        }

        public void ringBell() {
            System.out.println("Bicycle bell is ringing");
        }

        public void stop() {
            System.out.println("Bicycle is stopping");
        }
    }

