package com.xworkz.isarelation.ship;

public class Ship {

        public Ship() {
            System.out.println("Ship constructor is running");
        }

        public void sail() {
            System.out.println("Ship: Sailing on water...");
        }

        public void transportGoods() {
            System.out.println("Ship: Transporting goods...");
        }

        public void navigate() {
            System.out.println("Ship: Navigating using GPS and maps...");
        }

        public void anchor() {
            System.out.println("Ship: Dropping anchor...");
        }

        public void maintainBalance() {
            System.out.println("Ship: Maintaining balance in rough waters...");
        }
    }

