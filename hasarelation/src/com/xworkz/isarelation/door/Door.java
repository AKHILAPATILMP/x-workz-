package com.xworkz.isarelation.door;

public class Door {
        public Door() {
            System.out.println("Door constructor is running");
        }

        public void open() {
            System.out.println("Door is opened");
        }

        public void close() {
            System.out.println("Door is closed");
        }

        public void lock() {
            System.out.println("Door is locked");
        }

        public void unlock() {
            System.out.println("Door is unlocked");
        }

        public void knock() {
            System.out.println("Someone is knocking on the door");
        }
    }

