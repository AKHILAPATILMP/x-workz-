package com.xworkz.isarelation.train;

public class Train {
        public Train() {
            System.out.println("Train constructor is running");
        }

        public void start() {
            System.out.println("Train: Starting the journey...");
        }

        public void stop() {
            System.out.println("Train: Stopping at the station...");
        }

        public void accelerate() {
            System.out.println("Train: Speeding up...");
        }

        public void carryPassengers() {
            System.out.println("Train: Carrying passengers...");
        }

        public void blowHorn() {
            System.out.println("Train: Blowing the horn...");
        }
    }

