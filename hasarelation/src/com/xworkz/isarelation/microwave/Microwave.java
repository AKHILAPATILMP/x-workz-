package com.xworkz.isarelation.microwave;

public class Microwave {
        public Microwave() {
            System.out.println("Microwave constructor is running");
        }

        public void powerOn() {
            System.out.println("Microwave is turning on");
        }

        public void powerOff() {
            System.out.println("Microwave is turning off");
        }

        public void heatFood() {
            System.out.println("Microwave is heating food");
        }

        public void setTimer() {
            System.out.println("Microwave timer is set");
        }

        public void stopHeating() {
            System.out.println("Microwave has stopped heating");
        }
    }

