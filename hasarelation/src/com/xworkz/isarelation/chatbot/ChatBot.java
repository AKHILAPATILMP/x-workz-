package com.xworkz.isarelation.chatbot;

public class ChatBot {
        public ChatBot() {
            System.out.println("ChatBot constructor is running");
        }

        public void greetUser() {
            System.out.println("ChatBot: Hello! How can I assist you today?");
        }

        public void provideInformation() {
            System.out.println("ChatBot: Providing general information...");
        }

        public void answerFAQs() {
            System.out.println("ChatBot: Answering frequently asked questions...");
        }

        public void performBasicTasks() {
            System.out.println("ChatBot: Performing basic tasks like setting reminders...");
        }

        public void endConversation() {
            System.out.println("ChatBot: Thank you for chatting! Have a great day.");
        }
    }


