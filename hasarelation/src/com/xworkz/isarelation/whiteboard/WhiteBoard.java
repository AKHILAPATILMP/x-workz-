package com.xworkz.isarelation.whiteboard;

public class WhiteBoard {
        public WhiteBoard() {
            System.out.println("Whiteboard constructor is running");
        }

        public void write() {
            System.out.println("Writing on the whiteboard");
        }

        public void erase() {
            System.out.println("Erasing the content from the whiteboard");
        }

        public void draw() {
            System.out.println("Drawing on the whiteboard");
        }

        public void clean() {
            System.out.println("Cleaning the whiteboard");
        }

        public void display() {
            System.out.println("Whiteboard is displaying content");
        }
    }

