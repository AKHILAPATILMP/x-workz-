package com.xworkz.isarelation.bus;

public class Bus {
        public Bus() {
            System.out.println("Bus constructor is running");
        }

        public void start() {
            System.out.println("Bus is starting");
        }

        public void accelerate() {
            System.out.println("Bus is accelerating");
        }

        public void brake() {
            System.out.println("Bus is applying brakes");
        }

        public void honk() {
            System.out.println("Bus horn is sounding");
        }

        public void stop() {
            System.out.println("Bus is stopping");
        }

        public void openDoors() {
            System.out.println("Bus doors are opening");
        }

        public void closeDoors() {
            System.out.println("Bus doors are closing");
        }
    }

