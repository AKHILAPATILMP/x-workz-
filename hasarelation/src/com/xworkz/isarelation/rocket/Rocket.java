package com.xworkz.isarelation.rocket;

public class Rocket {

    public Rocket() {
        System.out.println("Rocket constructor is running");
    }

    public void launch() {
        System.out.println("Rocket is launching");
    }

    public void boost() {
        System.out.println("Rocket boosters are igniting");
    }

    public void enterOrbit() {
        System.out.println("Rocket is entering orbit");
    }

    public void deployPayload() {
        System.out.println("Rocket is deploying payload");
    }

    public void returnToEarth() {
        System.out.println("Rocket is returning to Earth");
    }
}

