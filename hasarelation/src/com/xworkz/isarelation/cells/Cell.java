package com.xworkz.isarelation.cells;

public class Cell {

        public Cell() {
            System.out.println("Cell constructor is running");
        }

        public void divide() {
            System.out.println("Cell: Undergoing cell division...");
        }

        public void grow() {
            System.out.println("Cell: Growing and developing...");
        }

        public void generateEnergy() {
            System.out.println("Cell: Producing energy through metabolism...");
        }

        public void communicate() {
            System.out.println("Cell: Communicating with other cells...");
        }

        public void repair() {
            System.out.println("Cell: Repairing itself...");
        }
    }

