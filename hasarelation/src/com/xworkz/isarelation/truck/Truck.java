package com.xworkz.isarelation.truck;

public class Truck {
        public Truck() {
            System.out.println("Truck constructor is running");
        }

        public void start() {
            System.out.println("Truck is starting");
        }

        public void accelerate() {
            System.out.println("Truck is accelerating");
        }

        public void brake() {
            System.out.println("Truck is applying brakes");
        }

        public void honk() {
            System.out.println("Truck horn is sounding");
        }

        public void stop() {
            System.out.println("Truck is stopping");
        }

        public void loadCargo() {
            System.out.println("Truck is loading cargo");
        }

        public void unloadCargo() {
            System.out.println("Truck is unloading cargo");
        }
    }

