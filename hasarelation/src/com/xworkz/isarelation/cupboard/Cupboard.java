package com.xworkz.isarelation.cupboard;

public class Cupboard {

        public Cupboard() {
            System.out.println("Cupboard constructor is running");
        }

        public void open() {
            System.out.println("Cupboard is opening");
        }

        public void close() {
            System.out.println("Cupboard is closing");
        }

        public void lock() {
            System.out.println("Cupboard is locked");
        }

        public void unlock() {
            System.out.println("Cupboard is unlocked");
        }
    }

