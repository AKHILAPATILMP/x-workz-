package com.xworkz.isarelation.bulb;

public class Bulb {
        public Bulb() {
            System.out.println("Bulb constructor is running");
        }

        public void turnOn() {
            System.out.println("Bulb is turned on");
        }

        public void turnOff() {
            System.out.println("Bulb is turned off");
        }

        public void glow() {
            System.out.println("Bulb is glowing");
        }

        public void dim() {
            System.out.println("Bulb brightness is dimmed");
        }

        public void flicker() {
            System.out.println("Bulb is flickering");
        }
    }

