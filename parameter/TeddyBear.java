class TeddyBear {
    
    public static void hug(String size) {
        System.out.println("Hugging a " + size + " teddy bear");
    }

    public static void gift(String occasion) {
        System.out.println("Gifted teddy bear for: " + occasion);
    }

    public static void color(String color) {
        System.out.println("Teddy bear color: " + color);
    }

    
    public static void play(String game, int duration) {
        System.out.println("Playing " + game + " with teddy for " + duration );
    }

    public static void wash(String soap, int temp) {
        System.out.println("Washing teddy with " + soap + " at " + temp );
    }

    
    public static void customize(String name, String outfit, double price) {
        System.out.println("Customized teddy as " + name + " with " + outfit + " for " + price);
    }

    public static void decorate(String material, int pieces, String pattern) {
        System.out.println("Decorating teddy with " + pieces + " " + material + " in " + pattern + " pattern");
    }
}